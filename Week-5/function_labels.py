# int() function
# Example of calling int()
num = int("5")

# Name: int
# Arguments: A number or string that looks like a number
# Side Effects: None
# Return Value: Returns the number as an integer


# str() function
# Example of calling str()
text = str(123)

# Name: str
# Arguments: Any value
# Side Effects: None
# Return Value: Returns the value as a string


# bool() function
# Example of calling bool()
check = bool(1)

# Name: bool
# Arguments: Any value
# Side Effects: None
# Return Value: Returns True or False depending on the value


# float() function
# Example of calling float()
decimal = float("3.14")

# Name: float
# Arguments: A number or string that looks like a number
# Side Effects: None
# Return Value: Returns the number as a float


# len() function
# Example of calling len()
length = len("hello")

# Name: len
# Arguments: A string, list, or something with length
# Side Effects: None
# Return Value: Returns how many items or characters there are


# pow() function
# Example of calling pow()
power = pow(2, 3)

# Name: pow
# Arguments: Two numbers (base and exponent)
# Side Effects: None
# Return Value: Returns the base raised to the exponent


# abs() function
# Example of calling abs()
positive = abs(-10)

# Name: abs
# Arguments: A number
# Side Effects: None
# Return Value: Returns the positive version of the number


